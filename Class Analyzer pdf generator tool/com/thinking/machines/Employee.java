package com.thinking.machines;
class Employee
{
String name;
String employeeId;
public void setId(String employeeId)
{
this.employeeId=employeeId;
}
public String getId()
{
return this.employeeId;
}
public void setName(String name)
{
this.name=name;
}
public String getName()
{
return this.name;
}
}